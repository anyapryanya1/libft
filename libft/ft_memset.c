#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*f;
	size_t	i;

	i = 0;
	f = (char *)b;
	while (i < len)
	{
		f[i] = c;
		i++;
	}
	return (b);
}
