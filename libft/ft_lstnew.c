#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = malloc(sizeof(*lst));
	if (lst == 0 || !lst)
		return (NULL);
	lst -> content = content;
	lst -> next = NULL;
	return (lst);
}
